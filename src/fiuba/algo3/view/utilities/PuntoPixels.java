package fiuba.algo3.view.utilities;

public class PuntoPixels {
	
	double x;
	double y;


	public PuntoPixels(int x,int y) {
		this.x = x;
		this.y = y;
	}
	
	public PuntoPixels(double x,double y) {
		this.x = x;
		this.y = y;
	}


	public double getX() {
		return x;
	}


	public double getY() {
		return y;
	}

}
